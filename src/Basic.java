//  BASIC SYNTAX
//  Every Java program must have:
//      Class
//      main() method (entry point)
//
//  Key Rules:
//      Java is case-sensitive
//      Every statement ends with ;
//      Execution starts from main()
//
//  Example:
class SyntaxDemo {
    public static void main(String[] args) {
        int number = 10;
        System.out.println("Number is: " + number);
    }
}

